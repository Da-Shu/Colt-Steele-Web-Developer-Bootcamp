//for loop
var colors = ["red", "white", "blue"];

for(var i = 0; i < colors.length; i++) {
	console.log(colors[i]);
}

//forEach
var colors = ["red", "white", "blue"];

colors.forEach(function(color) {
	console.log(color);
});
//"color" is just a name to define the elements; it could have been "item," or anything